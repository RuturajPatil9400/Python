my_list = [10, 20, 30, 40, 50]

print("Positive Slicing (my_list[1:4]):", my_list[1:4])
print("Positive Slicing (my_list[:3]):", my_list[:3])
print("Positive Slicing (my_list[2:]):", my_list[2:])

print("Negative Slicing (my_list[-4:-1]):", my_list[-4:-1])
print("Negative Slicing (my_list[-3:]):", my_list[-3:])
print("Negative Slicing (my_list[:-2]):", my_list[:-2])
