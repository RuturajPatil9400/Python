#Function of for and while loop

print("For loop:")
for i in range(1, 6):
    print(i)


print("\nWhile loop:")
j = 1
while j <= 5:
    print(j)
    j += 1


text = "Python"


print("Using for loop:")
for char in text:
    print(char)


print("\nUsing while loop:")
index = 0
while index < len(text):
    print(text[index])
    index += 1

