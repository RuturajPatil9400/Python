my_tuple = (10, 20, 30, 40, 50)

print("Positive Slicing (my_tuple[1:4]):", my_tuple[1:4])
print("Positive Slicing (my_tuple[:3]):", my_tuple[:3])
print("Positive Slicing (my_tuple[2:]):", my_tuple[2:])

print("Negative Slicing (my_tuple[-4:-1]):", my_tuple[-4:-1])
print("Negative Slicing (my_tuple[-3:]):", my_tuple[-3:])
print("Negative Slicing (my_tuple[:-2]):", my_tuple[:-2])
